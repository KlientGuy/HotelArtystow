    public function getAllFilePaths()
    {
        $dql = "
            SELECT p.path FROM App\Entity\Photo p
        ";

        $res = $this->getEntityManager()->createQuery($dql)->getResult();

        $toRet = [];
        foreach($res as $one)
        {
            $toRet[] = $one['path'];
        }

        return $toRet;
    }

